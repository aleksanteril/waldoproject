# Funktio joka tulostaa random vitsin 1-10 väliltä

def joke_for_waldo(num):
    print('\n')
    edellinen = num
    if num == 1:
        print('''Why don't scientists trust atoms?
        \t- Because they make up everything.''')
    elif num == 2:
        print('''Why did the scarecrow win an award?
        \t- Because he was outstanding in his field.''')
    elif num == 3:
        print('''What do you call fake spaghetti?
        \t- An impasta.''')
    elif num == 4:
        print('''Why don’t skeletons fight each other?
        \t- They don’t have the guts.''')
    elif num == 5:
        print('''What do you get when you cross a snowman and a vampire?
        \t- Frostbite.''')
    elif num == 6:
        print('''Why did the math book look sad?
        \t- Because it had too many problems.''')
    elif num == 7:
        print('''What do you call a bear with no teeth?
        \t- A gummy bear.''')
    elif num == 8:
        print('''Why don’t some couples go to the gym?
        \t- Because some relationships don’t work out.''')
    elif num == 9:
        print('''What do you call cheese that isn’t yours?
        \t- Nacho cheese.''')
    elif num == 10:
        print('''Why did the bicycle fall over?
        \t- Because it was two-tired.''')
    return edellinen